public class Accenture_eo {
    public static void main(String[] args) {
        int[] arr = {3,4,1,7,9};
        int n = arr.length;
        for(int i=0;i<n;i++){
            if(i%2==0){
                System.out.println(arr[i]);
            }else{
                System.out.println(arr[i]);
            }
        }
    }

}
